def bexp(z, L):
    return np.log(max(float(abs(complex(z))), 1e-300)) / np.log(L)


def setup(lam, n_modes):
    h_actual, idx, L = build(lam, n_modes, include_arith=True)
    h_model, _, _ = build(lam, n_modes, include_arith=False)
    prime = h_model - h_actual
    vals, vecs = eigh(h_actual)
    xi = vecs[:, 0]
    if xi[len(xi) // 2] < 0:
        xi = -xi
    xi = xi / norm(xi)
    d = 2.0 * np.pi * idx / L
    return h_actual.astype(complex), h_model.astype(complex), prime.astype(complex), idx.astype(int), d, L, xi.astype(complex)


def projector(q):
    return q.conj().T @ inv(q @ q.conj().T) @ q


def coeff_vector(const, linear):
    freqs = sorted(set(const.keys()) | set(linear.keys()))
    c = np.array([const.get(om, 0j) for om in freqs], dtype=complex)
    l = np.array([linear.get(om, 0j) for om in freqs], dtype=complex)
    return np.array(freqs), c, l


def moments(freqs, max_order):
    rows = []
    labels = []
    for part in ["c", "l"]:
        for k in range(max_order + 1):
            row_c = np.zeros(len(freqs), dtype=complex)
            row_l = np.zeros(len(freqs), dtype=complex)
            if part == "c":
                row_c = (1j * freqs) ** k
            else:
                row_l = (1j * freqs) ** k
            rows.append(np.concatenate([row_c, row_l]))
            labels.append((part, k))
    return np.vstack(rows), labels


def project_weight(weight, constraints):
    # Remove the row span generated by constraints from the weight vector.
    # Coefficient vectors satisfying constraints @ z = 0 are insensitive to
    # adding any vector in rowspan(constraints) to the weight.
    if constraints.size == 0:
        return weight
    q, _r = np.linalg.qr(constraints.conj().T, mode="reduced")
    return weight - q @ (q.conj().T @ weight)


